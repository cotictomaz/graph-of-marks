import os
import json
import statistics
from typing import List, Dict, Any, Optional

# Assicurati di importare i runner e i valutatori corretti
from gom.vqa.runner import run_vqa
from gom.vqa.evaluate import evaluate
from .models import OllamaVLM 

def run_ablation_experiments(
    experiment_name: str,
    ablation_grid: Dict[str, List[Any]],
    models_list: List[str],
    examples: List[Any],
    multimodal_prompt: str,
    preprocessor: Any = None,
    system_prompt: str = "",
    n_runs: int = 3,
    base_dir: str = "ablation_studies"
) -> None:
    """
    Executes the inference and evaluation phase for an ablation study.
    
    This function reads preprocessed artifacts generated by `generate_ablated_dataset`,
    runs inference using the specified VLMs, and calculates statistical metrics across multiple runs.
    
    The results are saved in the following structure:
    ablation_studies/
        results/
            {experiment_name}/
                {model_name}/
                    {ablation_value}/
                        summary_metrics.json
                        run_1/
                            raw_results.json
                        run_2/
                            raw_results.json
    """
    keys = list(ablation_grid.keys())
    value_lists = list(ablation_grid.values())
    
    print("\n" + "="*70)
    print(f"🚀 AVVIO INFERENZA: {experiment_name}")
    print("="*70)

    # 1. CICLO ESTERNO: I MODELLI (Ottimizza il caricamento in VRAM)
    for model_name in models_list:
        print(f"\n🤖 Inizializzazione Modello: {model_name}")
        current_model = OllamaVLM(model_name=model_name, system_prompt=system_prompt)
        safe_model_name = model_name.replace(":", "_")

        # 2. CICLO INTERMEDIO: I VALORI ABLATIVI
        for values_tuple in zip(*value_lists):
            current_overrides = dict(zip(keys, values_tuple))
            
            # Ricostruiamo ESATTAMENTE il nome della cartella del pre-processing
            folder_parts = []
            for k, v in current_overrides.items():
                safe_v = str(v).replace(" ", "").replace("[", "").replace("]", "").replace(",", "_")
                folder_parts.append(f"{k}_{safe_v}")
            folder_suffix = "_".join(folder_parts)
            
            # Percorso in cui SI TROVANO le immagini già disegnate
            preproc_dir = os.path.join(base_dir, "preprocessed_images", experiment_name, folder_suffix)
            
            if not os.path.exists(preproc_dir):
                print(f"  [⚠️ Warning] Cartella {preproc_dir} non trovata. Salto questa configurazione.")
                continue
                
            print(f"\n  📐 Parametri: {current_overrides}")
            print(f"  📂 Leggo le immagini da: {preproc_dir}")

            run_accuracies = []
            run_exacts = []

            # 3. CICLO INTERNO: LE RUN STATISTICHE
            for run_idx in range(1, n_runs + 1):
                # Prepariamo la cartella di output per questa specifica RUN
                out_run_dir = os.path.join(base_dir, "results", experiment_name, safe_model_name, folder_suffix, f"run_{run_idx}")
                os.makedirs(out_run_dir, exist_ok=True)
                
                out_json_path = os.path.join(out_run_dir, "raw_results.json")
                
                print(f"    🔄 Run {run_idx}/{n_runs}...")
                
                # --- IL CUORE DELLA FASE 2 ---
                # Chiamiamo run_vqa forzandolo a saltare la generazione grafica
                results = run_vqa(
                    examples=examples,
                    model=current_model,
                    preprocessor=preprocessor,
                    out_json=out_json_path,
                    prompt_tpl=multimodal_prompt,
                    batch_size=1,
                    preproc_folder=preproc_dir,      # Punta alle immagini pronte
                    include_scene_graph=True,
                    inference_image="preprocessed",
                    preproc_cfg=current_overrides,
                    skip_preproc=True                
                )

                # Calcolo delle metriche per questa run
                metrics = evaluate(results)
                acc = metrics.get('exact_percent', 0.0)
                exact = metrics.get('exact', 0)
                
                run_accuracies.append(acc)
                run_exacts.append(exact)
                print(f"       ↳ Risultato: {acc:.2f}% (Esatti: {exact})")

            # 4. AGGREGAZIONE E SALVATAGGIO SUMMARY JSON
            summary_stats = {
                "experiment_name": experiment_name,
                "model_name": model_name,
                "ablation_parameters": current_overrides,
                "n_runs": n_runs,
                "metrics": {
                    "mean_accuracy": statistics.mean(run_accuracies),
                    "std_accuracy": statistics.stdev(run_accuracies) if n_runs > 1 else 0.0,
                    "mean_exact_matches": statistics.mean(run_exacts)
                }
            }
            
            # Salviamo il file riassuntivo al livello della configurazione ablativa
            summary_dir = os.path.join(base_dir, "results", experiment_name, safe_model_name, folder_suffix)
            summary_path = os.path.join(summary_dir, "summary_metrics.json")
            
            with open(summary_path, "w", encoding="utf-8") as f:
                json.dump(summary_stats, f, indent=4)
                
    print("\n✅ Tutte le inferenze e le valutazioni completate!")